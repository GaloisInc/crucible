#![cfg_attr(not(with_main), no_std)]

#[cfg_attr(crux, crux_test)]
pub fn f() {
    assert!(u8::from_be_bytes([0x12]) == 0x12);
    assert!(u8::from_le_bytes([0x12]) == 0x12);

    assert!(u16::from_be_bytes([0x12, 0x34]) == 0x1234);
    assert!(u16::from_le_bytes([0x12, 0x34]) == 0x3412);

    assert!(u32::from_be_bytes([0x12, 0x34, 0x56, 0x78]) == 0x12345678);
    assert!(u32::from_le_bytes([0x12, 0x34, 0x56, 0x78]) == 0x78563412);

    assert!(u64::from_be_bytes([0x12, 0x34, 0x56, 0x78, 0x9a, 0xbc, 0xde, 0xf0]) == 0x123456789abcdef0);
    assert!(u64::from_le_bytes([0x12, 0x34, 0x56, 0x78, 0x9a, 0xbc, 0xde, 0xf0]) == 0xf0debc9a78563412);
}


#[cfg(with_main)] pub fn main() { println!("{:?}", f()); }
