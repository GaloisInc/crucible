#[cfg_attr(crux, crux::test)]
fn crux_test() {
    let s = format!("{:?} {:?}", 85000000000000000000000000000000000000u128, -85000000000000000000000000000000000000i128);
    assert!(&s == "85000000000000000000000000000000000000 -85000000000000000000000000000000000000");
}

pub fn main() {
    println!("{:?}", crux_test());
}
